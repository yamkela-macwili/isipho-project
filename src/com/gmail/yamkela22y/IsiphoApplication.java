    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int yesNo = JOptionPane.showConfirmDialog(this, "Do you want to delete ?", "Confirm", JOptionPane.YES_NO_CANCEL_OPTION);
        if (yesNo == JOptionPane.YES_OPTION) {
            try {
                String fName = null;
                String lName = null;
                String vill = null;
                String cellNo = txtCellNo.getText();
                gift = new Gift(fName, lName, vill, cellNo);
                tableLoad();    //Refresh the table
                if (!"".equals(cellNo)) {
                    delete(gift);
                    clear();
                    tableLoad();    //Refresh the table
                } else {
                    JOptionPane.showMessageDialog(null, "Please enter a number to delete a person");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

