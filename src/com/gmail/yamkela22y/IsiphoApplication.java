    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        PreparedStatement pstmt;
        Connection conn;
        try {
            String SQL = "select Name, Surname, village, cellNo from isipho2 where cellNo = ?";
            conn = getConnection();
            pstmt = conn.prepareStatement(SQL);
            pstmt.setString(1, txtSearch.getText());

            ResultSet rs = pstmt.executeQuery(); //use executeUpdate delete, insert update create

            if (rs.next() == true) {
                String fName = rs.getString(1);
                String lName = rs.getString(2);
                String village = rs.getString(3);
                String cellNo = rs.getString(4);

                txtfName.setText(fName);
                txtlName.setText(lName);
                txtvill.setText(village);
                txtCellNo.setText(cellNo);
            }

            close(conn, pstmt, null);

        } catch (SQLException e) {
            try {
                throw e;
            } catch (SQLException ex) {
                Logger.getLogger(IsiphoApplication.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(IsiphoApplication.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

