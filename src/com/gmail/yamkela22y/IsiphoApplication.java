    //Method that reads data from the database and load it to the table 
    public final void tableLoad() throws ClassNotFoundException {

        PreparedStatement pstmt;
        Connection conn;

        try {
            String SQL = "SELECT * FROM isipho_db.isipho2";
            conn = getConnection();
            pstmt = conn.prepareStatement(SQL);
            ResultSet rs = pstmt.executeQuery(); //use exectuteQuery for
            tblRegister.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, e);
        }
    }

